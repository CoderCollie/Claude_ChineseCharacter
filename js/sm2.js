  function migrate(hanjaData) {
    const sm2State = JSON.parse(localStorage.getItem(STORAGE_KEY) || '{}');
    const accState = JSON.parse(localStorage.getItem(ACC_KEY) || '{}');
    
    const needsMigration = Object.keys(sm2State).some(id => id.startsWith('h'));
    if (!needsMigration) return;

    console.log('Migrating study progress to new ID system...');
    
    const newSm2 = {};
    const newAcc = {};

    // Build Reverse Map: Char -> NewID
    const charToNewId = {};
    for (const h of hanjaData) {
      charToNewId[h.char] = h.id;
    }

    // 1. Migrate SM2 State
    for (const oldId in sm2State) {
      const char = OLD_ID_MAP[oldId];
      if (char) {
        const newId = charToNewId[char];
        if (newId) newSm2[newId] = sm2State[oldId];
      }
    }

    // 2. Migrate Accuracy State
    for (const oldId in accState) {
      const char = OLD_ID_MAP[oldId];
      if (char) {
        const newId = charToNewId[char];
        if (newId) newAcc[newId] = accState[oldId];
      }
    }

    localStorage.setItem(STORAGE_KEY, JSON.stringify(newSm2));
    localStorage.setItem(ACC_KEY, JSON.stringify(newAcc));
    _sm2Cache = newSm2;
    _accCache = newAcc;
    console.log('Migration complete!');
  }

