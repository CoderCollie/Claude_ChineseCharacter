  function _loadDailyStats() {
    try {
      let s = JSON.parse(localStorage.getItem('hanja_daily_stats') || 'null');
      if (!s) return { total: 0, history: {} };
      // 구형 포맷 { date, count, total } → 신형 { total, history } 마이그레이션
      if (s.date !== undefined && !s.history) {
        s = { total: s.total || 0, history: (s.date && s.count > 0) ? { [s.date]: s.count } : {} };
      }
      if (!s.history) s.history = {};
      return s;
    } catch { return { total: 0, history: {} }; }
  }

  function recordDailyQuiz() {
    const t = today();
    const s = _loadDailyStats();
    s.history[t] = (s.history[t] || 0) + 1;
    s.total = (s.total || 0) + 1;
    localStorage.setItem('hanja_daily_stats', JSON.stringify(s));
  }

  function getDailyStats() {
    const s = _loadDailyStats();
    return { today: s.history[today()] || 0, total: s.total || 0 };
  }

  function getRecentHistory(days) {
    days = days || 5;
    const DAY_LABELS = ['일', '월', '화', '수', '목', '금', '토'];
    const history = _loadDailyStats().history;
    const result = [];
    for (let i = days - 1; i >= 0; i--) {
      const d = new Date();
      d.setDate(d.getDate() - i);
      const dateStr = d.toISOString().split('T')[0];
      result.push({ date: dateStr, count: history[dateStr] || 0, label: i === 0 ? '오늘' : DAY_LABELS[d.getDay()] });
    }
    return result;
  }

  function recordStreak() {
    const t = today();
    let s;
    try { s = JSON.parse(localStorage.getItem('hanja_streak') || 'null'); } catch { s = null; }
    if (!s) s = { lastDate: null, count: 0, best: 0 };
    if (s.lastDate === t) return;
    const prev = new Date(); prev.setDate(prev.getDate() - 1);
    const yStr = prev.toISOString().split('T')[0];
    s.count = s.lastDate === yStr ? s.count + 1 : 1;
    s.lastDate = t;
    s.best = Math.max(s.best || 0, s.count);
    localStorage.setItem('hanja_streak', JSON.stringify(s));
  }

  function getStreak() {
    let s;
    try { s = JSON.parse(localStorage.getItem('hanja_streak') || 'null'); } catch { s = null; }
    if (!s) return 0;
    const prev = new Date(); prev.setDate(prev.getDate() - 1);
    const yStr = prev.toISOString().split('T')[0];
    return (s.lastDate === today() || s.lastDate === yStr) ? s.count : 0;
  }

  function getBestStreak() {
    let s;
    try { s = JSON.parse(localStorage.getItem('hanja_streak') || 'null'); } catch { s = null; }
    if (!s) return 0;
    return s.best || s.count || 0;
  }

  function getWeakCards(hanjaList) {
    const state = loadState();
    return hanjaList.filter(h => {
      const card = state[h.id];
      return card && (card.repetition === 0 || card.efactor < 2.0);
    });
  }

