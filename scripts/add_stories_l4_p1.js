let content = fs.readFileSync(filePath, 'utf8');

let count = 0;
for (const [char, story] of Object.entries(STORIES)) {
  const escaped = story.replace(/"/g, '\\"');
  // Add story before closing brace of entry with this char
  const pattern = new RegExp(`(char:"${char}",eumhun:"[^"]+",words:\\[[^\\]]+\\],busu:"[^"]+",similar:\\[[^\\]]*\\])(})`,'g');
  const newContent = content.replace(pattern, (match, p1, p2) => {
    if (match.includes('story:')) return match;
    count++;
    return `${p1},story:"${escaped}"${p2}`;
  });
  content = newContent;
}

fs.writeFileSync(filePath, content, 'utf8');
