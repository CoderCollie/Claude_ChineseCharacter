function parseUnihan() {
  // /tmp/kRSUnicode.txt: U+4E00\tkRSUnicode\t1.0  →  { '一': 1 }
  const map = {};
  const lines = fs.readFileSync('/tmp/kRSUnicode.txt', 'utf8').split('\n');
  for (const line of lines) {
    if (!line.startsWith('U+')) continue;
    const parts = line.split('\t');
    if (parts.length < 3) continue;
    const cp = parseInt(parts[0].slice(2), 16);
    const char = String.fromCodePoint(cp);
    // radical 번호에서 어포스트로피(') 제거 (변형 부수 표시)
    const radNum = parseInt(parts[2].split('.')[0].replace("'", ''));
    map[char] = radNum;
  }
  return map;
}

async function main() {
  console.log('Unihan kRSUnicode 데이터 로드 중...');
  const charToRadNum = parseUnihan();
  console.log('파싱 완료:', Object.keys(charToRadNum).length, '자');

  let updated = 0, skipped = 0, notFound = 0;

  for (let lv = 1; lv <= 8; lv++) {
    const filePath = path.join(__dirname, '../js/data/level' + lv + '.js');
    let content = fs.readFileSync(filePath, 'utf8');
    const varName = content.match(/const (HANJA_L\d) = \[/)[1];

    const itemRegex = /\{id:"(h\d+)",level:(\d+),char:"([^"]+)",eumhun:"([^"]+)",words:(\[.*?\]),busu:"([^"]*)",similar:(\[.*?\])\}/g;
    const items = [];
    let m;

    while ((m = itemRegex.exec(content)) !== null) {
      const [, id, level, char, eumhun, words, busu, similar] = m;

      let newBusu = busu;
      if (!busu || busu === '확인중' || busu === '확인필요') {
        const radNum = charToRadNum[char];
        if (radNum) {
          const radChar = RADICAL_CHAR[radNum];
          const radName = radChar ? B_NAMES[radChar] : null;
          if (radChar && radName) {
            newBusu = `${radChar}(${radName})`;
            updated++;
          } else {
            newBusu = busu || '확인중';
            notFound++;
          }
        } else {
          newBusu = busu || '확인중';
          notFound++;
        }
      } else {
        skipped++;
      }

      items.push(`{id:"${id}",level:${level},char:"${char}",eumhun:"${eumhun}",words:${words},busu:"${newBusu}",similar:${similar}}`);
    }

    fs.writeFileSync(filePath, `'use strict';\nconst ${varName} = [\n  ${items.join(',\n  ')}\n];\n`);
    console.log(`level${lv}.js 완료`);
  }

  console.log('\n=== 결과 ===');
  console.log('업데이트:', updated);
  console.log('기존 유지:', skipped);
  console.log('미발견:', notFound);
}

main().catch(console.error);
