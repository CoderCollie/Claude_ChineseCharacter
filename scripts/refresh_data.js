function updateFile(level) {
    const file = `level${level}.js`;
    console.log(`Updating ${file}...`);
    const filePath = path.join(__dirname, '../js/data', file);
    let content = fs.readFileSync(filePath, 'utf8');
    
    // 모든 객체 추출
    const newItems = [];
    const itemRegex = /\{id:"h\d+",level:\d,char:"(.)",eumhun:"(.*?)",words:(\[.*?\]),busu:".*?",similar:(\[.*?\])\}/g;
    
    let match;
    while ((match = itemRegex.exec(content)) !== null) {
        const char = match[1];
        const id = itemRegex.lastIndex; // 임시용이 아니라 매치된 문자열 전체를 활용하기 위해 regex.exec 사용
        
        // 캡처 그룹 재사용
        const fullMatch = match[0];
        const idStr = fullMatch.match(/id:"(h\d+)"/)[1];
        const levelNum = fullMatch.match(/level:(\d)/)[1];
        const eumhunStr = match[2];
        const wordsArr = match[3];
        const similarArr = match[4];
        
        let busu = getRadical(char);
        
        newItems.push(`{id:"${idStr}",level:${levelNum},char:"${char}",eumhun:"${eumhunStr}",words:${wordsArr},busu:"${busu}",similar:${similarArr}}`);
    }
    
    if (newItems.length > 0) {
        const varName = `HANJA_L${level}`;
        const output = `'use strict';\nconst ${varName} = [\n  ${newItems.join(',\n  ')}\n];\n`;
        fs.writeFileSync(filePath, output);
    }
}

