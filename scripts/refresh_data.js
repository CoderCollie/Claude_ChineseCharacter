    // 하지만 대부분의 폰트 데이터에서 이들은 별도의 유니코드로 존재하지 않고 
    // 글자 안에 녹아있음.
    
    if (bestBusu) return `${bestBusu}(${B_NAMES[bestBusu]})`;
    
    // 4. 정말 못 찾겠는 경우 (상형문자의 변형 등)
