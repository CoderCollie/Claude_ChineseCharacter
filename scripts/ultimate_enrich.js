function process(filePath) {
  console.log(`Processing ${filePath}...`);
  const content = fs.readFileSync(filePath, 'utf8');
  const varMatch = content.match(/const (HANJA_L\d) = \[/);
  if (!varMatch) return;
  const varName = varMatch[1];
  
  const itemRegex = /\{id:"(h\d+)",level:(\d),char:"(.*?)",eumhun:"(.*?)",words:(\[.*?\]),busu:".*?",similar:(\[.*?\])\}/g;
  const items = [];
  let match;
  
  while ((match = itemRegex.exec(content)) !== null) {
    const [full, id, level, char, eumhun, words, similar] = match;
    let busu = inferBusu(char, eumhun);
    if (!busu) busu = "확인중";
    items.push(`{id:"${id}",level:${level},char:"${char}",eumhun:"${eumhun}",words:${words},busu:"${busu}",similar:${similar}}`);
  }
  
  fs.writeFileSync(filePath, `'use strict';\nconst ${varName} = [\n  ${items.join(',\n  ')}\n];\n`);
}

